#include <unistd.h>

void	ft_putnbr(int nb)
{
	char			x;

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0 )
	{
		nb = -nb;
		write (1, "-", 1);
		ft_putnbr(nb);
	}
	else if (nb < 10)
	{
		x = '0' + nb;
		write (1, &x, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
