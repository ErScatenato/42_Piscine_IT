int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while (i * i <= nb)
		{
			if (i > 46340)
				return (0);
			else if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
